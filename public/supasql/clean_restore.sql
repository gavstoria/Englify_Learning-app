-- Projek_DPSI - Supabase Restore Script
-- ============================================
-- 
-- Apa yang di-restore:
--   ✅ Semua definisi tabel (CREATE TABLE)
--   ✅ Fungsi (handle_new_user, update_user_points)
--   ✅ Data games preset (4 game bawaan)
--   ✅ Data quizzes, quiz_questions, quiz_options
--   ✅ Primary keys, constraints, foreign keys
--   ✅ Row Level Security
-- 
-- Apa yang TIDAK di-restore (tidak bisa dipindah):
--   ❌ profiles (bergantung pada auth.users)
--   ❌ classes (bergantung pada profiles)
--   ❌ class_members (bergantung pada profiles)
--   ❌ class_assignments (bergantung pada classes)
--   ❌ user_activities (bergantung pada auth.users)
--   ❌ custom games (dibuat oleh teacher yang perlu daftar ulang)
--
-- Setelah restore: semua anggota tim daftar ulang →
-- profil baru otomatis dibuat via trigger handle_new_user()
-- ============================================

SET client_encoding = 'UTF8';
SET standard_conforming_strings = on;

-- ============================================
-- FUNCTIONS
-- ============================================

CREATE OR REPLACE FUNCTION public.handle_new_user() RETURNS trigger
    LANGUAGE plpgsql SECURITY DEFINER
    SET search_path TO 'public'
    AS $$
begin
  insert into public.profiles (id, full_name, role, total_points, point_achievement, games_played, badge)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    'student',
    0,
    0,
    0,
    'Bronze'
  );
  return new;
end;
$$;

CREATE OR REPLACE FUNCTION public.update_user_points(user_id_input uuid, points_to_add integer) RETURNS void
    LANGUAGE plpgsql
    AS $$
begin
  update public.profiles
  set
    total_points = total_points + points_to_add,
    games_played = games_played + 1
  where id = user_id_input;
end;
$$;

